    private void Setup_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Setup_ButtonActionPerformed
        Database_URL = D_URL.getText();
        Database_Username = Username.getText();
        Database_Password = Password.getText();
        Server_URL = S_URL.getText();
        DownloadPath = DownloadPath_Field.getText();

        if(Supporter.saveConfig(Database_URL, Database_Username, Database_Password, Server_URL, DownloadPath))
        {
            Final.setText("Success!");
        }
        else{
            Final.setText("Failed!");
        }
    }//GEN-LAST:event_Setup_ButtonActionPerformed

    public boolean testServer(String serverAddress, int port) {
        try (Socket socket = new Socket(serverAddress, port)) {
            return true;
        } catch (Exception e) {
            return false;
        }
    }

    public boolean testNetworkPath(String path) {
        File file = new File(path);
        return file.exists();
    }

    public static boolean testConnection(String url, String username, String password) {
        try (Connection connection = DriverManager.getConnection(url, username, password)) {
            return true;
        } catch (SQLException e) {
            return false;
        }
    }

