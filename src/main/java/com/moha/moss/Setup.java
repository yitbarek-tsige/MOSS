    private void Test1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Test1ActionPerformed
        
      Database_URL = D_URL.getText();
      Database_Username = Username.getText();
      Database_Password = Password.getText();   
      
      if(testConnection(Database_URL, Database_Username, Database_Password)){
      DB_Label.setText("Successful!");
      DB_Label.setForeground(Color.GREEN); 
      DB_Label.setFont(DB_Label.getFont().deriveFont(Font.BOLD));}
      else{
      DB_Label.setText("Error!");
      DB_Label.setForeground(Color.RED); 
      DB_Label.setFont(DB_Label.getFont().deriveFont(Font.BOLD));}
    }//GEN-LAST:event_Test1ActionPerformed

    private void Local_Server_CheckBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Local_Server_CheckBoxActionPerformed
        if (Local_Server_CheckBox.isSelected()) {
            S_URL.setText("");
            S_URL.setEnabled(false);
            Add.setEnabled(true); 
            Test2.setEnabled(false);
        } else {
            S_URL.setEnabled(true); 
            Add.setEnabled(false); 
            Test2.setEnabled(true);
        }
    }//GEN-LAST:event_Local_Server_CheckBoxActionPerformed

    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY); // Only allow folders
        int option = fileChooser.showOpenDialog(null);
        if (option == JFileChooser.APPROVE_OPTION) {
            File selectedFolder = fileChooser.getSelectedFile();
            S_URL.setText(selectedFolder.getAbsolutePath());
        }
    }//GEN-LAST:event_AddActionPerformed

    private void Test2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Test2ActionPerformed
        Server_URL = S_URL.getText();
        String serverAddress = Server_URL.split("/")[2]; // "192.168.1.5"
        if(testServer(serverAddress, 445) && testNetworkPath(Server_URL))
        {
            S_Label.setText("Successful!");
            S_Label.setForeground(Color.GREEN);
            S_Label.setFont(DB_Label.getFont().deriveFont(Font.BOLD));}
        else{
            S_Label.setText("Error!");
            S_Label.setForeground(Color.RED);
            S_Label.setFont(DB_Label.getFont().deriveFont(Font.BOLD));}
    }//GEN-LAST:event_Test2ActionPerformed

